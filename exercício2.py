def valores(num):
    a, b = 0, 1
    while b < num:
        a, b = b, a + b
    if b == num:
        return True
    else:
        return False

num = int(input("Insira um número: "))
if valores(num):
    print(f"{num} faz parte da sequência Fibonacci.")
else:
    print(f"{num} não faz parte da sequência de Fibonacci.")
